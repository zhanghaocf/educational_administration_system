$("#loginBtn").on('click',function(){
	var user=$('#user').val();
	var pass=$('#pass').val();
	var code=$("#code").val();
	if(user==""||pass==""){
		alert('用户名或密码不能为空！！');
	}else if(code==""){
		alert("请填写验证码");
	}else{
		$.post('/ZH/educational_administration_system/index.php/Home/Index/login',{'user':user,'pass':pass,'code':code},function(data){
				if(data){
					if(data=="1"){
						alert("用户或密码错误");
						refreshFn();
					}else{
						window.location.href="/ZH/educational_administration_system/index.php/"+data;
					}
				}else{
					alert("验证码错误");
					refreshFn();
				}
			});
	}
